                        if all_text[j][1] == 's' and all_text[j][2] == 'c' and all_text[j][3] == 'r' and all_text[j][4] == 'i' and all_text[j][5] == 'p' and all_text[j][6] == 't':
                            css_or_script = 1  # 如果是script  css_or_script = 1
